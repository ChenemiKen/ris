    {
        $this->authorize('is-admin');
        $request->validate([
            'date' => ['required', 'date'],
            'title' => ['required', 'string', 'max:255'],
            'description' => ['required', 'string'],
        ]);

        // persist
        $event = Event::create([
            'date' => $request->date,
            'title' => $request->title,
            'description' => $request->description,
        ]);
        return redirect()->route('calendar')->with('success','Event added successfully!');
    }

